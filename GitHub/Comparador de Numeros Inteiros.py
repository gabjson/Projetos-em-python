"""

Descrição : 
Faça um programa que leia dois valores inteiros (x e y) e
imprima na tela o resultado das seguintes comparações entre estes dois valores, exatamente nesta ordem:

x é maior que y

x é igual a y

x é menor que y

x é diferente de y

x é maior ou igual a y

x é menor ou igual a y

"""

N1 = int(input())
N2 = int(input())

if N1 > N2 :
    print('1')
else :
    print('0')
if N1 == N2 :
    print('1')
else :
    print('0')
if N1 < N2 :
    print('1')
else :
    print('0')
if N1 != N2 :
    print('1')
else :
    print('0')
if N1 >= N2 :
    print('1')
else :
    print('0')
if N1 <= N2 :
    print('1')
else :
    print('0')
